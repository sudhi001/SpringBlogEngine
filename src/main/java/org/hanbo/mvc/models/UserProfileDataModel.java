   public String getUserIconId()
   {
      return userIconId;
   }

   public void setUserIconId(String userIconId)
   {
      this.userIconId = userIconId;
   }

   public String getUserEmail()
   {
      return userEmail;
   }

   public void setUserEmail(String userEmail)
   {
      this.userEmail = userEmail;
   }
